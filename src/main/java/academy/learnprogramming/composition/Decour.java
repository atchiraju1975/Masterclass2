package academy.learnprogramming.composition;

public class Decour {

    private String wallColour;
    private String wallHangings;
    private String curtains;
    private String lighting;

    public Decour(String wallColour, String wallHangings, String curtains, String lighting) {
        this.wallColour = wallColour;
        this.wallHangings = wallHangings;
        this.curtains = curtains;
        this.lighting = lighting;
    }
}
